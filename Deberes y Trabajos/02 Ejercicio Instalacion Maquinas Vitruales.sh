# Comandos y explicacion para instalar VMs de Linux Ubuntu server 22, windows 10 y windows server 2022 en mi maquina fisica windows 11.

# 1. Instalar VirtualBox
# Descargar VirtualBox desde la pagina oficial: https://www.virtualbox.org/
# Ejecutar el instalador y seguir las instrucciones en pantalla para completar la instalacion.

# 2. Descargar la imagen ISO de Ubuntu Server 22, Windows 10 y Windows Server 2022
# Descargar la imagen ISO de Ubuntu Server 22 desde la pagina oficial: https://ubuntu.com/download/server
# Guardar la imagen ISO en una ubicacion facil de encontrar.
# Descargar la imagen ISO de Windows 10 desde la pagina oficial: https://www.microsoft.com/es-es/software-download/windows10
# Guardar la imagen ISO en una ubicacion facil de encontrar.
# Descargar la imagen ISO de Windows Server 2022 desde la pagina oficial: https://www.microsoft.com/es-es/evalcenter/evaluate-windows-server-2022
# Guardar la imagen ISO en una ubicacion facil de encontrar.

# 3. Crear una nueva maquina virtual para Ubuntu Server 22
# Abrir VirtualBox y hacer clic en "Nueva" para crear una nueva maquina virtual.
# Asignar un nombre a la maquina virtual (por ejemplo, "Ubuntu Server 22").
# Seleccionar "Linux" como tipo de sistema operativo y "Ubuntu (64-bit)" como version.
# Configurar la cantidad de memoria RAM que se desea asignar a la maquina virtual (recomendado al menos 2 GB).
# Crear un disco duro virtual (VDI) y asignar un tamaño adecuado (recomendado al menos 20 GB).
# Seleccionar la imagen ISO de Ubuntu Server 22 como disco de arranque.
# Hacer clic en "Crear" para finalizar la configuracion de la maquina virtual.

# 4. Crear una nueva maquina virtual para Windows 10
# Abrir VirtualBox y hacer clic en "Nueva" para crear una nueva maquina virtual.
# Asignar un nombre a la maquina virtual (por ejemplo, "Windows 10").
# Seleccionar "Windows" como tipo de sistema operativo y "Windows 10 (64-bit)" como version.
# Configurar la cantidad de memoria RAM que se desea asignar a la maquina virtual (recomendado al menos 4 GB).
# Crear un disco duro virtual (VDI) y asignar un tamaño adecuado (recomendado al menos 50 GB).
# Seleccionar la imagen ISO de Windows 10 como disco de arranque.
# Hacer clic en "Crear" para finalizar la configuracion de la maquina virtual.

# 5. Crear una nueva maquina virtual para Windows Server 2022
# Abrir VirtualBox y hacer clic en "Nueva" para crear una nueva maquina virtual.
# Asignar un nombre a la maquina virtual (por ejemplo, "Windows Server 2022").
# Seleccionar "Windows" como tipo de sistema operativo y "Windows Server 2022 (64-bit)" como version.
# Configurar la cantidad de memoria RAM que se desea asignar a la maquina virtual (recomendado al menos 4 GB).
# Crear un disco duro virtual (VDI) y asignar un tamaño adecuado (recomendado al menos 50 GB).
# Seleccionar la imagen ISO de Windows Server 2022 como disco de arranque.
# Hacer clic en "Crear" para finalizar la configuracion de la maquina virtual.

# 6. Iniciar las maquinas virtuales
# Seleccionar la maquina virtual de Ubuntu Server 22 y hacer clic en "Iniciar".
# Seguir las instrucciones en pantalla para completar la instalacion de Ubuntu Server 22.
# Repetir el proceso para las maquinas virtuales de Windows 10 y Windows Server 2022.
# 7. Configurar las maquinas virtuales
# Una vez que las maquinas virtuales esten instaladas, se pueden configurar segun las necesidades.
